def mood_responses(mood):
    if mood == "happy":
        print("Such a beautiful day!")
    elif mood == "sad":
        print("Brighter horizons lay ahead!")
    elif mood == "mad":
        print("As unsettled as waves in a storm!")
    elif mood == "scared":
        print("Courage comes from admitting and confronting our fears!")
    else:
        print("Please input a proper mood: happy, sad, mad or scared.")
